void GraphHallo::init()
{
	CanvasInt.init(1,2000);
}

void GraphHallo::draw()
{
	int del = 2000;
	for (int j = 0; j < 200; j++)
	{
		CanvasInt.init(1,del);
		for (int i = 0; i < sizeof(hallo) / sizeof(hallo[0]); i++)
		{
			CanvasInt.line(
				hallo[i][0] << 5,
				hallo[i][1] << 5,
				hallo[i][2] << 5,
				hallo[i][3] << 5);
		}
		del = del * 8 / 10;
	}
}

